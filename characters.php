<h2>The Main Characters</h2>

<h3>FFI</h3>
<p>There is not a specific main character in Final Fantasy I. However, one could consider the four warriors of light to be the main unit.</p>
<img class="characterPic" src="ff1c.jpg" alt="ff character">

<h3>FFII</h3>
<p id="firion">Firion is the main character in Final Fantasy II. Firion has light brown skin, brown eyes, and white hair tied in a ponytail and covered by a 
    blue, black, and orange bandana. Firion always tries to protect the world and the people he loves. In the Japanese release of the game, it is stated that Firion 
    has a weakness for women, which could explain why he was so easily seduced by the Lamia Queen.</p>
<img class="characterPic" src="ff2c.png" alt="ff character">

<h3>FFIII</h3>
<p>There is not a specific main character in Final Fantasy III. However, one could consider the four Onion Kids to be the main unit.</p>
<img class="characterPic" src="ff3c.png" alt="ff character">

<h3>FFIV</h3>
<p id="cecil">Cecil Harvey is the main character of Final Fantasy IV, who appears in the interquel Final Fantasy IV -Interlude- that bridges the gap between the two games. 
    Cecil is one of the few characters in the series to change his job during the game, starting out as a Dark Knight, but after a trial and the battle with the 
    first of the Four Elemental Archfiends, transforms into a Paladin.</p>
<img class="characterPic" src="ff4c.jpg" alt="ff character">

<h3>FFV</h3>
<p id="bartz">Bartz Klauser is the main character in Final Fantasy V. His parents died when he was very young, but he decides to explore the world anyway after 
remembering some advice his father gave him.</p>
<img class="characterPic" src="ff5c.png" alt="ff character">

<h3>FFVI</h3>
<p id="terra">Terra is the main character in Final Fantasy VI. She was kidnapped by the government and used as a tool of war. She broke out of it and escapes the 
grasp of the government. She finds a group of friends and they all work together to save the world.</p>
<img class="characterPic" src="ff6c.png" alt="ff character">

<h3>FFVII</h3>
<p id="cloud">Cloud Strife is the main character of Final Fantasy VII. He joins an elite force named SOLDIER and meets a man named Sephiroth, who eventually 
turns out to be his arch nemesis. He fights his way through many obstacles, and in the end, comes to a great resolution about his conflicting personality and memories.</p>
<img class="characterPic" src="ff7c.jpg" alt="ff character">

<h3>FFVIII</h3>
<p id="squall">Squall Leonhart is the main protagonist of Final Fantasy VIII. A young and aloof SeeD mercenary, others view Squall as cold and focused until 
    he begins to question the way he has chosen to live his life. Squall's weapon, the gunblade, is one of the iconic weapons of the Final Fantasy series. 
    Squall is later given the title Commander.</p>
<img class="characterPic" src="ff8c.png" alt="ff character">

<h3>FFIX</h3>
<p id="zidane">Zidane Tribal is the main character of Final Fantasy IX. He is a thief who works for the Tantalus Theater Troupe set to kidnap the princess of Alexandria, an event that escalates to a quest to protect the planet of Gaia</p>
<img class="characterPic" src="ff9c.png" alt="ff character">

<h3>FFX</h3>
<p id="tidus">Tidus is the main character of Final Fantasy X. He finds himself lost in the dimension of time and tries to find his way back to his time. Along the way, he meets 
friends and goes on an epic journey to save the world from Sin. He has an annoying personality.</p>
<img class="characterPic" src="ff10c.png" alt="ff character">

<h3>FFXI</h3>
<p>There is not a specific main character in Final Fantasy XI. The game is considered to be an MMORPG, so technically, the player's own character is portrayed to be the 
main character in their own story.</p>
<img class="characterPic" src="ff11c.jpg" alt="ff character">

<h3>FFXII</h3>
<p id="vaan">Vaan is the main character in Final Fantasy XII. An orphan street thief hailing from Rabanastre in the Kingdom of Dalmasca, Vaan hates the Archadian Empire for the death of his brother, Reks, 
    and the subjugation of his home, and dreams of one day owning an airship and being a sky pirate free to travel the world.</p>
<img class="characterPic" src="ff12c.png" alt="ff character">

<h3>FFXIII</h3>
<p class="lightning">Lightning is the main character in Final Fantasy XIII. She has a tough and cold personality, and often tells people directly how she is feeling. 
She is a part of her planet's elite army, but at some point in the past, has become infected the the evil monsters that inhabited the world. She now bands up with other people 
that had been infected and attempts to rid the world of the monster that created all this.</p>
<img class="characterPic" src="ff13c.png" alt="ff character">

<h3>FFXIV</h3>
<p>There is not a specific main character in Final Fantasy XIV. Because it is an MMORPG, the player's own character is seen to be the main character, and they play out their 
own story.</p>
<img class="characterPic" src="ff14c.jpg" alt="ff character">
